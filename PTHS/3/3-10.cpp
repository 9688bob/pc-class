#include <iostream>
using namespace std;

int main() {
    int A = 10, B = 20;
    cout << "A = 10, B = 20" << endl;
    cout << "A = B   is " << (A == B) << endl;
    cout << "A != B  is " << (A != B) << endl;
    cout << "A < B   is " << (A < B) << endl;
    //code by. PTHS s110149
    cout << "A > B   is " << (A > B) << endl;
    cout << "A <= B  is " << (A <= B) << endl;
    cout << "A >= B  is " << (A >= B) << endl;

    return 0;
}